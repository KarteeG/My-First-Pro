package com.typecast;

public class Type_Casting {
	public static void main(String[] args) { 
		int a=121; 					//convert one date type to another data type
		long b=a;
		System.out.println(b);   //Widerring (Small to Big)
		float c=1845.4878f;
		double d=(float)c;
		System.out.println(d);	//NArrowign (Big to Small)
		  
	}

}
